<script>
    function togglePhoneNumber() {
        const phoneLink = document.getElementById('phone-link');
        const colon = document.getElementById('colon');
        const isVisible = phoneLink.style.opacity === '1';

        if (isVisible) {
            // Hide smoothly
            phoneLink.style.opacity = '0';
            phoneLink.style.maxWidth = '0';
            colon.style.opacity = '0';
            setTimeout(() => phoneLink.style.overflow = 'hidden', 300);
        } else {
            // Show smoothly
            phoneLink.style.overflow = 'visible';
            phoneLink.style.maxWidth = '200px';
            phoneLink.style.opacity = '1';
            colon.style.opacity = '1';
        }
    }

    // Optional: hide number on mobile by default
    function handlePhoneVisibility() {
        const phoneLink = document.getElementById('phone-link');
        const colon = document.getElementById('colon');
        if (window.innerWidth < 768) {
            phoneLink.style.opacity = '0';
            phoneLink.style.maxWidth = '0';
            colon.style.opacity = '0';
        }
    }

    handlePhoneVisibility();
    window.addEventListener('resize', handlePhoneVisibility);
</script>
